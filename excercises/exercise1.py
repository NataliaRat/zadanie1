#kwadrat
bok_a = 7
pole_kw = bok_a * bok_a
print("Pole kwadratu wynosi: "+str(pole_kw))

#prostokąt
bok_b = 4
bok_c = 10
pole_pr = bok_b * bok_c
print("Pole prostokąta wynosi: "+str(pole_pr))

#trójkąt
podstawa = 6
wysokosc = 8
pole_tr = 0.5 * podstawa * wysokosc
